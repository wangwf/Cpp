import xml.etree.ElementTree as ET
import pprint

def count_tags(filename):
        # YOUR CODE HERE
        tree = ET.parse(filename)
        root=tree.getroot()
        output={}
        for t in root:
            if t.tag in output.keys():
                output[t.tag] +=1
            else:
                output[t.tag]=1
        return output
        

def test():

    tags = count_tags('example.osm')
    pprint.pprint(tags)
